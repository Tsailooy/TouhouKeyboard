/**
  * @brief  The application entry point.
  * @retval int
  */
int main(void)
{
  HAL_Init();

  SystemClock_Config();

  MX_GPIO_Init();
  MX_USB_DEVICE_Init();
  
  uint8_t buffer[8]={0x00,0x00,0x00,0x00,0x00,0x00,0x00,0x00};
  uint8_t keyCode[6];

  KEY_Init();
  
  while (1)
  {
    
    KEY_Scan();
    //按键滤波和检测由systick完成
    keyCode[0] = KEY_FIFO_get();
    keyCode[1] = KEY_FIFO_get();
    keyCode[2] = KEY_FIFO_get();
    keyCode[3] = KEY_FIFO_get();
    keyCode[4] = KEY_FIFO_get();
    keyCode[5] = KEY_FIFO_get();

    for(int idx=0;idx < 6;idx++)
    {
      if(keyCode[idx] != KEY_NONE)
      {
        switch (keyCode[idx])
        {
        case KEY_1_UP:
          buffer[2 + idx] = 0x00;
          break;
        case KEY_1_DOWN:
          buffer[2 + idx] = 0x50;
          break;
        case KEY_2_UP:
          buffer[3 + idx] = 0x00;
          break;
        case KEY_2_DOWN:
          buffer[3 + idx] = 0x51;
          break;
        case KEY_3_UP:
          buffer[4 + idx] = 0x00;
          break;
        case KEY_3_DOWN:
          buffer[4 + idx] = 0x52;
          break;
        case KEY_4_UP:
          buffer[5 + idx] = 0x00;
          break;
        case KEY_4_DOWN:
          buffer[5 + idx] = 0x4F;
          break;
        default:
          break;
        }
      }
    }
    
    USBD_HID_SendReport(&hUsbDeviceFS,buffer,8);
    
  }
  
}

